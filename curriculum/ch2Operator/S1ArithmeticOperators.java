package curriculum.ch2Operator;
/*
연산자는 변수 또는 값에 대한 연산을 수행하는 기호 또는 키워드입니다.
자바에서는 다양한 종류의 연산자가 제공되며, 이를 통해 산술, 비교, 논리, 대입 등 다양한 연산을 수행할 수 있습니다.
 */

// 산술 연산자 (Arithmetic Operator)
public class S1ArithmeticOperators {
    public static void main(String[] args) {
        // 산술 연산자 예제
        System.out.println(5 + 3); // 8
        System.out.println(5 - 3); // 2
        System.out.println(5 * 3); // 15
        System.out.println(5 / 3); // 1 (몫: 자바스크립트와 다름)
        System.out.println(5 % 3); // 2 (나머지)
    }
}
